$(document).ready(function(){

    /////// --- ACCOUNT START --- \\\\\\\
    $('#account').mouseover(function(){

        $('#account .account-menu').fadeIn("fast");

    });

    /////// --- ACCOUNT MENU --- \\\\\\\
    $('#account .account-menu').mouseleave(function(){

        $('#account .account-menu').fadeOut("fast");

    });

    /////// ### ACCOUNT END ### \\\\\\\

});